public class Ex5_DataType {
    public static void main(String[] args) {
        double a = 10.1; // 실수는 기본적으로 double에 저장.
        float b = 10f; //F,f 를 작성하면 4byte float에 저장.
        System.out.println("a = "+a);
        System.out.println("b = "+b);
    }
    
}
